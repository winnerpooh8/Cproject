	int hexa = 0x6; //숫자앞에 0x가 붙으면 16진수
	
	printf("8진수 octo = %o\n", octo);
	printf("16진수 hexa = %\n", hexa);
	printf("8진수 표기법 출력 octo= %#o\n", octo); 
